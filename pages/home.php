											</br></br>
											<h3>关于插件兼容性及图标</h3>
											<p>Bukkit 是由 Mojang 支持的一个项目，它为 Minecraft 服务器提供了一个可扩展的插件系统。</br>
											Spigot 是基于 Bukkit 的一个分支，由 CraftBukkit 的原作者之一开发。</br>
											Paper 是另一个基于 Spigot 的分支。Paper 的目标是进一步提升服务器性能，并添加一些额外的功能和优化选项。</br>
											也就是说兼容关系大致如下：</br>
											Paper <strong>理论上</strong>兼容所有插件, Spigot 兼容 Bukkit 插件 , Bukkit 不完全兼容 Spigot 插件,至于到底兼容什么，可以在<a href="https://mineplugin.org/">Minecraft 插件百科</a>上查询</br>
											如果服务端旁边带有&nbsp;<span class="iconfont icon-paperplane-fill" style="font-size:14px;"></span>&nbsp;图标,就代表它支持所有插件</br>
											如果服务端旁边带有&nbsp;<span class="iconfont icon-duoxiancheng" style="font-size:25px;color:#b381de;"></span>&nbsp;图标，就表示它是一个创新的多线程服务端</br>
											如果服务端旁边带有&nbsp;<span class="iconfont icon-cuowu" style="font-size:25px;color:red;"></span>&nbsp;图标,则代表它已经停止维护
